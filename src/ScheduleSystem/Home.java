    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // Récupérer les valeurs des combobox
        // Récupérer les valeurs des combobox
        String sqlMatiere = "SELECT intitule FROM matiere ";
        String sqlClasse = "SELECT intitule FROM classe";
        String sqlAnneeScolaire = "SELECT dateDebut FROM anneescolaire";
        String sqlEnseignant = "SELECT nom FROM enseignant";

        try {
            Statement statementClasse;
            ResultSet resultSetClasse;
            Statement statementAnneeScolaire;
            ResultSet resultSetAnneeScolaire;
            Statement statementEnseignant;
            ResultSet resultSetEnseignant;
            try ( // Exécuter les requêtes SQL
                     Statement statementMatiere = con.createStatement();  ResultSet resultSetMatiere = statementMatiere.executeQuery(sqlMatiere)) {
                statementClasse = con.createStatement();
                resultSetClasse = statementClasse.executeQuery(sqlClasse);
                statementAnneeScolaire = con.createStatement();
                resultSetAnneeScolaire = statementAnneeScolaire.executeQuery(sqlAnneeScolaire);
                statementEnseignant = con.createStatement();
                resultSetEnseignant = statementEnseignant.executeQuery(sqlEnseignant);
                // Ajouter les valeurs récupérées aux JComboBox correspondants

                while (resultSetMatiere.next()) {
                    String intituleMatiere = resultSetMatiere.getString("intitule");
                    jComboBox4.addItem(intituleMatiere);

                }
                while (resultSetClasse.next()) {
                    String intituleClasse = resultSetClasse.getString("intitule");
                    jComboBox7.addItem(intituleClasse);

                }
                while (resultSetAnneeScolaire.next()) {
                    String dateDebut = resultSetAnneeScolaire.getString("dateDebut");
                    jComboBox6.addItem(dateDebut);

                }
                while (resultSetEnseignant.next()) {
                    String nomEnseignant = resultSetEnseignant.getString("nom");
                    jComboBox5.addItem(nomEnseignant);

                }
                // Fermer les ResultSet et les Statements
            }

            resultSetClasse.close();
            statementClasse.close();

            resultSetAnneeScolaire.close();
            statementAnneeScolaire.close();

            resultSetEnseignant.close();
            statementEnseignant.close();

        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton10ActionPerformed

